package day07_relational_operators;

public class UpdatedVariables {
    public static void main(String[] args) {

        int a = 10;
        a = 11;

        a++; // reassign  a = a + 1;

        ++a; // reassign

        int x = 10;
        x = x + 4;

        System.out.println(x);   // 14

        x += 4;
        System.out.println(x);



    }
}
