package day3_comments_escape_sequence;

public class TabAndNextLine {

    public static void main(String [] args){
// This is not tabbed
        System.out.println("This is not tabbed");

        //This is tabbed
        System.out.println("\tThis is tabbed");

        //This is with two tab
        System.out.println("\t\t This is tabbed twice");

        //Examples of new line
        System.out.println();
        System.out.println("1) Go to parking lot\n");
        System.out.println("2) Find your car\n");
        System.out.println("3) Start engine");
    }

}
